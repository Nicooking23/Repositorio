valores = [5, 2, 8, 1, 9, 3]
#mostrando lista inicial
print(valores)
#ordeno lista menor a mayor 
valores.sort()
#imprime lista ordenada 
print(valores)
#invertir la lista 
valores.reverse()
#imprimir valores inversos 
print(valores)
#obtiene el valor minimo 
minimo = min(valores)
#imprime el valor minimo
print(minimo)
#obtengo el valor maximo 
maximo = max(valores)
#imprimo el valor maximo 
print(maximo)